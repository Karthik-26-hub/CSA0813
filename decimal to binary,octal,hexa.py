def decimaltobinary(n):
    decimal=n
    print(decimal,"in binary: ",bin(n))
def decimaltooctal(n):
    decimal=n
    print(decimal,"in octal: ",oct(n))
def decimaltohexa(n):
    decimal=n
    print(decimal,"in hexadecimal:",hex(n))
n=15
decimaltobinary(n)
decimaltooctal(n)
decimaltohexa(n)
